public class LATIHAN1 {

    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        System.out.println("10 + 5 = " + (x + y));
        System.out.println("10 - 5 = " + (x - y));
        System.out.println("10 x 5 = " + (x * y));
        System.out.println("10 : 5 = " + (x / y));
        System.out.println("10 % 5 = " + (10 % 5));
    }
}
